#list organization
intrested_areas = ['gotham','amboseli park','mombasa','kiganjo']
print(intrested_areas)
#sorted()
#1. alphabetical order
print(sorted(intrested_areas))
print(intrested_areas)
#2reversed alphabetical
print(intrested_areas)
#reverse
intrested_areas.reverse()
print(intrested_areas)
intrested_areas.reverse()
print("Here's the original list:")
print(intrested_areas)
#sort()
print("original list:")
intrested_areas.sort()
print("List arranged in descending alphabetical order:")
print(intrested_areas)
print("List in ascending alphabetical order:")
intrested_areas.sort(reverse=True)
print(intrested_areas)
