#add whitespaces- character combination \n and \t are used
print('dave\nkaranja') # \n - adds newline
print('dave\tkaranja') # \t - adds tab in a line
print('\t\ndave\t\nkaranja') # \t\n -adds both tab and newline

#remove whitespaces
# removetemporarily
#from the right
f_l = 'python '
f_l.rstrip
print(f_l)
#from the left
f_l =' python'
f_l.lstrip
print(f_l)
#from both sides
f_l = ' python '
f_l.strip()
print(f_l)
#remove permanently
f_l = ' python '
f_l = f_l.strip()
print(f_l)